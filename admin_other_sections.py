import streamlit as st

OTHER_SECTION_LABELS = [
    "Мелкие",
    "Средние",
    "Цветные",
    "Боковые",
    "Парные",
    "Эксклюзив",
]


def render_other_sections_page() -> None:
    st.markdown("### Другие разделы")
    st.caption("Отдельный будущий контур для разделов, которые не относятся к текущему управлению товаром.")

    st.info(
        "Сейчас рабочее “Управление товаром” относится только к двум категориям: "
        "Основной каталог и Крупные. Для остальных разделов будет отдельная загрузка и отдельные правила."
    )

    st.markdown("#### Разделы вне текущего Product Management")
    for label in OTHER_SECTION_LABELS:
        st.write(f"- {label}")

    st.warning(
        "Импорт для этих разделов пока не включён намеренно. Это защищает основной каталог от случайной загрузки "
        "мелких, цветных, парных или боковых камней через неправильный сценарий."
    )

    st.markdown("#### Что будет добавлено позже")
    st.write("- отдельные Excel-шаблоны;")
    st.write("- отдельные обязательные поля;")
    st.write("- отдельные правила публикации;")
    st.write("- отдельная проверка цен и наличия.")
